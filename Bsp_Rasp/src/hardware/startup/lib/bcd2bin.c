#include "startup.h"

unsigned
bcd2bin(unsigned a) {
	return ((a & 0xf000) >> 12) * 1000 + ((a & 0xf00) >> 8) * 100 + ((a & 0xf0) >> 4) * 10 + (a & 0xf);
}

